public class Exercise2 {

    public static void main(String[] args) {

        double Fahrenheit = 100.0;
        // Her har jeg angivet værdien for variablen Fahrenheit.
        double celsius = (Fahrenheit - 32) * 5 / 9;
        // Jeg har fundet formlen for at omregne fahrenheit til celsius.
        // Her efter har jeg angivet variablen celsius til at være lig med formlen.

        System.out.println(celsius);
        // Her har jeg udskrevet værdien af celsius
        System.out.println(Fahrenheit + " grader fahrenheit svare til " + celsius + " grader celcius");
        // Her har jeg skrevet min sætning med resultater ud i console.

    }


}
